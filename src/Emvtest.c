void LoadTestKeys(void)
{
	int		iCnt;
	CAPKLIST    stTemCAPK;

	iCnt=0;
	while(1)
	{
		if (AllEmvCapk[iCnt].KeyID==0 || AllEmvCapk[iCnt].KeyValue==NULL)
		{
			break;
		}
		memcpy((char*)&stTemCAPK,AllEmvCapk[iCnt].KeyValue,sizeof(CAPKLIST));
		emv_add_one_ca_public_key(&stTemCAPK);
		iCnt++;
	}
}

